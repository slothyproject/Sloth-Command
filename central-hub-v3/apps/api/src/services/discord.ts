/**
 * Start a Discord bot
 */
export async function startBot(botId: string): Promise<boolean> {
  const bot = await prisma.discordBot.findUnique({
    where: { id: botId },
  });

  if (!bot) {
    throw new Error('Bot not found');
  }

  const botDir = path.join(BOTS_DIR, botId);

  try {
    // Check if already running
    if (bot.status === 'running') {
      console.log(`Bot ${bot.name} is already running`);
      return true;
    }

    // Install dependencies
    console.log(`📦 Installing dependencies for ${bot.name}...`);
    await execAsync('npm install', { cwd: botDir });

    // Start bot as separate process
    console.log(`🚀 Starting bot: ${bot.name}`);
    const process = exec('npm start', { cwd: botDir });

    // Store process info
    await prisma.discordBot.update({
      where: { id: botId },
      data: {
        status: 'running',
        processId: process.pid?.toString(),
      },
    });

    // Handle process output
    process.stdout?.on('data', (data) => {
      console.log(`[${bot.name}] ${data}`);
    });

    process.stderr?.on('data', (data) => {
      console.error(`[${bot.name}] ERROR: ${data}`);
    });

    process.on('exit', async (code) => {
      console.log(`Bot ${bot.name} exited with code ${code}`);
      await prisma.discordBot.update({
        where: { id: botId },
        data: { status: code === 0 ? 'stopped' : 'error' },
      });
    });

    return true;
  } catch (error) {
    console.error(`Failed to start bot ${bot.name}:`, error);
    
    await prisma.discordBot.update({
      where: { id: botId },
      data: {
        status: 'error',
        lastError: error instanceof Error ? error.message : 'Unknown error',
      },
    });
    
    return false;
  }
}

/**
 * Stop a Discord bot
 */
export async function stopBot(botId: string): Promise<boolean> {
  const bot = await prisma.discordBot.findUnique({
    where: { id: botId },
  });

  if (!bot) {
    throw new Error('Bot not found');
  }

  try {
    if (bot.processId) {
      // Kill the process
      process.kill(parseInt(bot.processId), 'SIGTERM');
    }

    await prisma.discordBot.update({
      where: { id: botId },
      data: {
        status: 'stopped',
        processId: null,
      },
    });

    console.log(`✅ Stopped bot: ${bot.name}`);
    return true;
  } catch (error) {
    console.error(`Failed to stop bot ${bot.name}:`, error);
    return false;
  }
}

