/**
 * Delete a bot
 */
export async function deleteBot(botId: string): Promise<boolean> {
  const bot = await prisma.discordBot.findUnique({
    where: { id: botId },
  });

  if (!bot) {
    return false;
  }

  // Stop if running
  if (bot.status === 'running') {
    await stopBot(botId);
  }

  // Delete files
  const botDir = path.join(BOTS_DIR, botId);
  try {
    await rm(botDir, { recursive: true, force: true });
  } catch {
    // Ignore errors
  }

  // Delete from DB
  await prisma.discordBot.delete({
    where: { id: botId },
  });

  console.log(`✅ Deleted bot: ${bot.name}`);
  return true;
}

/**
 * Update bot configuration
 */
export async function updateBotConfiguration(
  botId: string,
  config: Partial<BotConfiguration>
): Promise<DiscordBot> {
  const bot = await prisma.discordBot.findUnique({
    where: { id: botId },
  });

  if (!bot) {
    throw new Error('Bot not found');
  }

  const currentConfig = bot.configuration as any || {};
  const updatedConfig = { ...currentConfig, ...config };

  const updated = await prisma.discordBot.update({
    where: { id: botId },
    data: {
      configuration: updatedConfig,
      aiEnabled: config.aiEnabled ?? bot.aiEnabled,
    },
  });

  // Regenerate bot files
  await generateBotFiles(updated);

  // Restart if running
  if (bot.status === 'running') {
    await stopBot(botId);
    await startBot(botId);
  }

  return updated;
}

