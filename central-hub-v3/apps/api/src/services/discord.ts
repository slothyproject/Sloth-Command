// Bot deployment directory
const BOTS_DIR = process.env.BOTS_DIR || './bots';

interface BotConfiguration {
  prefix?: string;
  adminRoles?: string[];
  logChannel?: string;
  welcomeChannel?: string;
  aiEnabled: boolean;
  moderationEnabled: boolean;
  customCommands: Array<{
    name: string;
    response: string;
  }>;
}

interface BotStatus {
  status: 'running' | 'stopped' | 'error' | 'restarting';
  pid?: number;
  uptime?: number;
  memoryUsage?: number;
  messageCount: number;
  errorCount: number;
  lastError?: string;
  guilds: Array<{
    id: string;
    name: string;
    memberCount: number;
  }>;
}

/**
 * Create a new Discord bot
 */
export async function createBot(
  name: string,
  token: string,
  config: Partial<BotConfiguration> = {}
): Promise<DiscordBot> {
  // Validate token by making a test request to Discord API (with circuit breaker)
  try {
    const botData = await withDiscordCircuitBreaker(async () => {
      const response = await fetch('https://discord.com/api/v10/users/@me', {
        headers: { 'Authorization': `Bot ${token}` },
      });

      if (!response.ok) {
        throw new Error('Invalid Discord bot token');
      }

      return await response.json();
    }) as any;

    // Create bot record
    const bot = await prisma.discordBot.create({
      data: {
        name,
        token, // In production, encrypt this
        clientId: botData.id,
        status: 'stopped',
        configuration: {
          prefix: '!',
          aiEnabled: true,
          moderationEnabled: true,
          customCommands: [],
          ...config,
        } as any,
        aiEnabled: config.aiEnabled ?? true,
      },
    });

    // Generate bot files
    await generateBotFiles(bot);

    console.log(`✅ Created Discord bot: ${name} (ID: ${bot.id})`);
    return bot;
  } catch (error) {
    console.error('❌ Failed to create bot:', error);
    throw error;
  }
}

/**
 * Generate bot files from template
 */
async function generateBotFiles(bot: DiscordBot): Promise<void> {
  const botDir = path.join(BOTS_DIR, bot.id);
  
  await mkdir(botDir, { recursive: true });

  // Generate package.json
  const packageJson = {
    name: `discord-bot-${bot.id}`,
    version: '1.0.0',
    description: bot.name,
    main: 'index.js',
    scripts: {
      start: 'node index.js',
      dev: 'nodemon index.js',
    },
    dependencies: {
      'discord.js': '^14.14.1',
      'axios': '^1.6.2',
      'dotenv': '^16.3.1',
      'express': '^4.18.2',
    },
  };

  await writeFile(
    path.join(botDir, 'package.json'),
    JSON.stringify(packageJson, null, 2)
  );

  // Generate main bot file
  const botCode = generateBotCode(bot);
  await writeFile(path.join(botDir, 'index.js'), botCode);

  // Generate .env file
  const envContent = `DISCORD_TOKEN=${bot.token}
BOT_ID=${bot.id}
CENTRAL_HUB_API_URL=${process.env.CENTRAL_HUB_API_URL || 'http://localhost:3001'}
OLLAMA_HOST=${process.env.OLLAMA_HOST || 'http://localhost:11434'}
AI_ENABLED=${bot.aiEnabled}
`;
  await writeFile(path.join(botDir, '.env'), envContent);

  // Generate command handlers
  await writeFile(
    path.join(botDir, 'commands.js'),
    generateCommandsCode(bot)
  );
}

